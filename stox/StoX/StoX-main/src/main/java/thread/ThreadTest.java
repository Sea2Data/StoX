    private void jRunActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jRunActionPerformed
        if (runner.getFinished()) {
            runner.setFinished(false);
            runner.setPause(false);
            runner.setFunc(1);
            runner.execute();
            jF1.setSelected(true);
        } else if (runner.getPause()) {
            if (jParameter.isVisible() && !jParameter.getText().isEmpty()) {
                runner.setParameter(jParameter.getText());
            }
            runner.setPause(false);
            jLblParameter.setVisible(false);
            jParameter.setVisible(false);
        }
        onUpdateButtons();
    }//GEN-LAST:event_jRunActionPerformed

    private void jStopActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jStopActionPerformed
        runner.setFinished(true);
        jFinished.setSelected(true);
        createNewRunner();
        onUpdateButtons();
    }//GEN-LAST:event_jStopActionPerformed

    private void jResetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jResetActionPerformed
        jReady.setSelected(true);
        createNewRunner();
        onUpdateButtons();
    }//GEN-LAST:event_jResetActionPerformed

    private void onUpdateButtons() {
        jStop.setEnabled(runner.isRunning());
        jReset.setEnabled(runner.getFinished());
        jRun.setText(runner.getFinished() ? "Run" : runner.getPause() ? "Continue" : "Pause");
    }

    public void propertyChange(PropertyChangeEvent evt) {
        if (evt.getPropertyName().equalsIgnoreCase("get-param-by-user")) {
            jLblParameter.setVisible(true);
            jParameter.setVisible(true);
            jParameter.requestFocus();
            onUpdateButtons();
        } else if (evt.getPropertyName().equalsIgnoreCase("function-end")) {
            switch ((Integer) evt.getNewValue()) {
                case 1:
                    jF2.setSelected(true);
                    break;
                case 2:
                    jF3.setSelected(true);
                    break;
                case 3:
                    jFinished.setSelected(true);
                    onUpdateButtons();
                    break;
            }
        }

    }

