    private void jSelectFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jSelectFileActionPerformed
        final JFileChooser fc = new JFileChooser();
        File f = new File(jFile.getText());
        if (f.exists()) {
            if (f.isFile()) {
                f = f.getParentFile();
            }
        } else {
            f = new File(defPath);
        }
        fc.setCurrentDirectory(f);

        int returnVal = fc.showOpenDialog(SelectFileDlg.this);
        if (returnVal == JFileChooser.APPROVE_OPTION) {
            String path = fc.getSelectedFile().getPath().replace("\\", "/");
            path = path.replace(project.getProjectFolder().replace("\\", "/") + "/", "");
            path = path.replace(ProjectUtils.getSystemFolder(), "${STOX}");
            jFile.setText(path);
        }
    }//GEN-LAST:event_jSelectFileActionPerformed

